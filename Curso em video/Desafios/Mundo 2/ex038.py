#Recebe os valores
num1 = float(input('Digite o primeiro valor! '))
num2 = float(input('Digite o segundo valor! '))

#Faz o teste logico
if num1 >  num2:
    print('O primeiro valor é maior que o segundo! ')
elif num1 < num2:
    print('O segundo valor é maior que o primeiro! ')
else:
    print('Os dois valores são iguais')